package pemogramanStruktur

/**
 * variable yang memiliki pengambalian/hasil boolean (true atau false)
 * >
 * <
 * ==
 * !=
 * >=
 * <=
 * */

fun main() {

    val angkaPertama = 10
    val angkaKedua = 8

    println(angkaPertama > angkaKedua)
    println(angkaPertama < angkaKedua)
    println(angkaPertama == angkaKedua)
    println(angkaPertama != angkaKedua)
    println(angkaPertama >= angkaKedua)
    println(angkaPertama <= angkaKedua)


}