package OperatorOperation

fun main() {
    /**
     * remainder (modulus) adalah sisa bagi dari hasil pembagian 2 variable
     * menggunakan simbol %
     * */

    val angkaPertama = 8
    val angkaKedua = 3

    println("sisa hasil bagi dari $angkaPertama dan $angkaKedua adalah ${angkaPertama % angkaKedua}")
}